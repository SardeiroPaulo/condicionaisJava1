// número par ou impar com if/else

package application;

public class Condi1 {
    public static void main(String[] args) {

        int n = 11;

        if ( n % 2 ==0 ){
            System.out.println("Número par");
        }
        else if ( n % 2 != 0 ){
            System.out.println("Número ímpar");
        }

    }
}
